    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // assigning values to variables
        String name = nameField.getText();
        String phone = phoneField.getText();
        String address = addressField.getText();

        if (!name.equals("")) {

                try {
                    //PREPARING QUERY
                    ps = con.prepareStatement("insert into tbl_publisher(name, phone, address) values (?, ?, ?)");
                    ps.setString(1, name);
                    ps.setString(2, phone);
                    ps.setString(3, address);
                    int res = ps.executeUpdate();

                    if (res == 1) {
                        loadPublisher();
                        JOptionPane.showMessageDialog(this, "Publisher Added Successfully!");
                        nameField.setText("");
                        phoneField.setText("");
                        addressField.setText("");
                        nameField.requestFocus();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error While Adding Publisher!", "Error", JOptionPane.ERROR_MESSAGE);
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(Publisher.class.getName()).log(Level.SEVERE, null, ex);
                }
        } else {
            JOptionPane.showMessageDialog(jPanel1, "Name Field is Empty!", "Error", JOptionPane.WARNING_MESSAGE);
            nameField.requestFocus();
        }

    }//GEN-LAST:event_addBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel d1 = (DefaultTableModel) pubTable.getModel();
        int sIndex = pubTable.getSelectedRow();
        String name = d1.getValueAt(sIndex, 1).toString();
        int id = Integer.parseInt(d1.getValueAt(sIndex, 0).toString());

        int confirm = JOptionPane.showConfirmDialog(jPanel1, "Are you sure want to Delete " + name + "?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {

            try {
                //PREPARING QUERY
                ps = con.prepareStatement("delete from tbl_publisher where pub_id = ?");
                ps.setInt(1, id);
                int res = ps.executeUpdate();

                if (res == 1) {

                    loadPublisher();
                    JOptionPane.showMessageDialog(this, "Publisher Deleted Successfully!");
                    nameField.setText("");
                    phoneField.setText("");
                    addBtn.setEnabled(true);
                    deleteBtn.setEnabled(false);
                    addBtn.setForeground(new Color(102, 102, 255));
                    nameField.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(this, "Error While Deleting Publisher!", "Error", JOptionPane.ERROR_MESSAGE);
                }

            } catch (SQLException ex) {
                Logger.getLogger(Publisher.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_deleteBtnActionPerformed

