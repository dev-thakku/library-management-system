    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel d1 = (DefaultTableModel) pubTable.getModel();

        //ASSIGNING THE SELECTED ROW
        int sIndex = pubTable.getSelectedRow();

        //ASSIGNING THE ID OF SELECTED Publisher
        int id = Integer.parseInt(d1.getValueAt(sIndex, 0).toString());

        // ASSIGNING VALUES TO THE VARIABLES
        String name = nameField.getText();
        String phone = phoneField.getText();
        String address = addressField.getText();
        
        if (!name.equals("")) {

                try {
                    //PREPARING QUERY
                    ps = con.prepareStatement("update tbl_publisher set name =  ?, phone = ?, address = ? where pub_id = ?");
                    ps.setString(1, name);
                    ps.setString(2, phone);
                    ps.setString(3, address);
                    ps.setInt(4, id);
                    int res = ps.executeUpdate();

                    if (res == 1) {
                        loadPublisher();
                        JOptionPane.showMessageDialog(this, "Publisher Updated Successfully!");
                        nameField.setText("");
                        phoneField.setText("");
                        addressField.setText("");
                        updateBtn.setEnabled(false);
                        addBtn.setEnabled(true);
                        addBtn.setForeground(new Color(102, 102, 255));
                        nameField.requestFocus();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error While Updating Publisher!");
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(Publisher.class.getName()).log(Level.SEVERE, null, ex);
                }
            }  else {
            JOptionPane.showMessageDialog(jPanel1, "First Name Field is Empty!", "Error", JOptionPane.WARNING_MESSAGE);
            nameField.requestFocus();
        }

    }//GEN-LAST:event_updateBtnActionPerformed

