    //Load Catogory into Table
    public void loadAuthor() {

        try {
            ps = con.prepareStatement("select * from tbl_author");
            rs = ps.executeQuery();

            //GETTING THE TABLE MODEL OF catTable
            DefaultTableModel d1 = (DefaultTableModel) authorTable.getModel();
            d1.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();
                v2.add(rs.getString("auth_id"));
                v2.add(rs.getString("fname"));
                v2.add(rs.getString("lname"));
                d1.addRow(v2);
            }

        } catch (SQLException ex) {
            Logger.getLogger(Author.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

