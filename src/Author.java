    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel d1 = (DefaultTableModel) authorTable.getModel();

        //ASSIGNING THE SELECTED ROW
        int sIndex = authorTable.getSelectedRow();

        //ASSIGNING THE ID OF SELECTED AUTHOR
        int id = Integer.parseInt(d1.getValueAt(sIndex, 0).toString());

        // ASSIGNING VALUES TO THE VARIABLES
        String fname = fnameField.getText();
        String lname = lnameField.getText();

        if (!fname.equals("")) {

                try {
                    //PREPARING QUERY
                    ps = con.prepareStatement("update tbl_author set fname =  ?, lname = ? where auth_id = ?");
                    ps.setString(1, fname);
                    ps.setString(2, lname);
                    ps.setInt(3, id);
                    int res = ps.executeUpdate();

                    if (res == 1) {
                        loadAuthor();
                        JOptionPane.showMessageDialog(this, "Author Updated Successfully!");
                        fnameField.setText("");
                        lnameField.setText("");
                        updateBtn.setEnabled(false);
                        addBtn.setEnabled(true);
                        addBtn.setForeground(new Color(102, 102, 255));
                        fnameField.requestFocus();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error While Updating Author!");
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(Author.class.getName()).log(Level.SEVERE, null, ex);
                }
            }  else {
            JOptionPane.showMessageDialog(jPanel1, "First Name Field is Empty!", "Error", JOptionPane.WARNING_MESSAGE);
            fnameField.requestFocus();
        }

    }//GEN-LAST:event_updateBtnActionPerformed

